export const getRegionsList = () => {
  return Object.entries(saudiRegions).map(([key, region]) => ({
    value: key,
    label: region.name
  }));
};

export const getCitiesByRegion = (regionKey) => {
  const region = saudiRegions[regionKey];
  if (!region) return [];

  return region.cities.map(city => ({
    value: typeof city === 'string' ? city : city.name,
    label: typeof city === 'string' ? city : city.name
  }));
};

export const getDistrictsByCity = (regionKey, cityName) => {
  const region = saudiRegions[regionKey];
  if (!region) return [];

  const city = region.cities.find(c =>
    (typeof c === 'string' ? c : c.name) === cityName
  );

  if (!city || typeof city === 'string') return [];

  return city.districts.map(district => ({
    value: district,
    label: district
  }));
};
